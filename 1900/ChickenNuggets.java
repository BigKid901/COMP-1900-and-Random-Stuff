//This program gives you a number of chicken nuggets that you can 
//	"make" using thighs and wings.
public class ChickenNuggets {
	public static void main(String[] args) {
		int chickenNuggets;
		int thigh = 7;
		int wing = 3;

		chickenNuggets = (thigh * 7) + (wing * 2);

		System.out.print(chickenNuggets);
	}
}
